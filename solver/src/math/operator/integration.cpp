    /**
     * @brief Compute Legendre rule for reference edge {[0],[1]}.
     * 
     * source: https://people.sc.fsu.edu/~jburkardt/datasets/quadrature_rules_tri/quadrature_rules_tri.html
     *
     * @param order Polynomial order.
     * @return reference to hard-coded std::vector<Integration_Point>.
     * @throws std::invalid_argument if quadrature of the given order not implemented.
     * @note Integration_Point = {x, y, z, weight}, in 2D case, z=0.
     */
    std::vector<Integration_Point> get_integration_points_edge(int order)
    {
        std::vector<double> x(order);
        std::vector<double> w(order);
        legendre_rule(order, 0, 1, x.data(), w.data());

        std::vector<Integration_Point> legendre(order);

        for(int i=0; i<order; ++i)
        {
            legendre[i] = {{x[i], 0., 0.}, w[i]};
        }
        return legendre;    
    }



